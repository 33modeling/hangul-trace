/*
 * 획 경로 데이터 — 자모 24 + 숫자 10.
 *
 * 각 글자 = 획 배열. 각 획 = 정규화 좌표 [x, y] 점들의 폴리라인(0~1, y는 아래로).
 * 점 순서가 곧 긋는 방향이다(번호 배지는 첫 점, 화살표는 마지막 점에 표시).
 * 곡선(ㅇ ㅎ 원, 숫자 곡선)은 _soArc 로 점들을 생성한다.
 *
 * 좌표는 글자가 대략 0.2~0.8 박스에 들어오도록 잡았고, core.js 의
 * playStrokeOrderAnim 이 캔버스 버퍼 크기로 환산해 그린다.
 */

/** 타원호 점 생성. deg 기준: 0=오른쪽, 90=아래, -90=위. */
function _soArc(cx, cy, rx, ry, deg0, deg1, steps) {
  const n = steps || 22;
  const a0 = deg0 * Math.PI / 180;
  const a1 = deg1 * Math.PI / 180;
  const pts = [];
  for (let i = 0; i <= n; i++) {
    const t = a0 + (a1 - a0) * (i / n);
    pts.push([cx + rx * Math.cos(t), cy + ry * Math.sin(t)]);
  }
  return pts;
}

/** 전체 원(위에서 시계방향). */
function _soCircle(cx, cy, rx, ry) {
  return _soArc(cx, cy, rx, ry, -90, 270, 26);
}

